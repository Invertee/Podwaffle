'use strict';

const fs = require('fs');
const path = require('path');

const mobileRoot = path.resolve(__dirname, '..');
const repoRoot = path.resolve(mobileRoot, '..');
const clientDir = path.join(repoRoot, 'client');
const targetDir = path.join(mobileRoot, 'www');
const configPath = path.join(mobileRoot, 'server.config.json');
const bulmaCachePath = path.join(mobileRoot, '.cache', 'bulma-1.0.2.min.css');
const bulmaUrl = 'https://cdn.jsdelivr.net/npm/bulma@1.0.2/css/bulma.min.css';

async function ensureBulma() {
  if (fs.existsSync(bulmaCachePath)) return bulmaCachePath;
  console.log('[mobile] Downloading pinned Bulma 1.0.2 asset...');
  const response = await fetch(bulmaUrl);
  if (!response.ok) {
    throw new Error(`Failed to download Bulma: HTTP ${response.status}`);
  }
  fs.mkdirSync(path.dirname(bulmaCachePath), { recursive: true });
  fs.writeFileSync(bulmaCachePath, await response.text(), 'utf8');
  return bulmaCachePath;
}

async function main() {
  if (!fs.existsSync(path.join(clientDir, 'index.html'))) {
    throw new Error(`Client source not found at ${clientDir}`);
  }

  const bulmaSource = await ensureBulma();
  const config = fs.existsSync(configPath)
    ? JSON.parse(fs.readFileSync(configPath, 'utf8'))
    : {};

  fs.rmSync(targetDir, { recursive: true, force: true });
  fs.cpSync(clientDir, targetDir, {
    recursive: true,
    filter: (source) => !source.split(path.sep).includes('tests'),
  });

  fs.mkdirSync(path.join(targetDir, 'css'), { recursive: true });
  fs.copyFileSync(bulmaSource, path.join(targetDir, 'css', 'bulma.min.css'));

  const configuredBackendUrl = String(process.env.PODWAFFLE_BACKEND_URL || config.backendUrl || '').trim();
  const legacyServerUrl = String(config.serverUrl || '').trim();
  const backendUrl = configuredBackendUrl
    || (/github\.io/i.test(legacyServerUrl) ? '' : legacyServerUrl);
  const profileId = String(config.profileId || config.profileGuid || '').trim();
  const mobileConfig = {
    backendUrl,
    profileId,
    generatedAt: new Date().toISOString(),
  };

  const bootstrap = `/* Generated by scripts/sync-web-assets.js. */
(function bootstrapPodwaffleMobile() {
  const config = ${JSON.stringify(mobileConfig, null, 2)};
  const managedBackendKey = 'podwaffle_mobile_managed_backend_url';
  window.PODWAFFLE_MOBILE_CONFIG = config;
  window.IS_PODWAFFLE_MOBILE_BUILD = true;

  if (config.profileId && !localStorage.getItem('podwaffle_guid')) {
    localStorage.setItem('podwaffle_guid', config.profileId);
  }

  const previousManagedUrl = localStorage.getItem(managedBackendKey) || '';
  if (!config.backendUrl) {
    const existingRaw = localStorage.getItem('podwaffle_server_connection');
    let existing = null;
    try { existing = existingRaw ? JSON.parse(existingRaw) : null; } catch (_) {}
    const existingOrigin = existing && existing.host
      ? (existing.secure ? 'https' : 'http') + '://' + existing.host + (existing.port ? ':' + existing.port : '')
      : '';

    // Remove only the backend injected by an earlier packaged build. Manually
    // configured server connections are preserved.
    if (previousManagedUrl && existingOrigin === previousManagedUrl) {
      localStorage.removeItem('podwaffle_server_connection');
    }
    localStorage.removeItem(managedBackendKey);
    return;
  }

  try {
    const url = new URL(config.backendUrl);
    const existingRaw = localStorage.getItem('podwaffle_server_connection');
    let existing = null;
    try { existing = existingRaw ? JSON.parse(existingRaw) : null; } catch (_) {}

    const existingOrigin = existing && existing.host
      ? (existing.secure ? 'https' : 'http') + '://' + existing.host + (existing.port ? ':' + existing.port : '')
      : '';
    const legacyOrMissing = !existing || !existing.host || /github\\.io/i.test(existingOrigin);
    const managedByPreviousBuild = !!previousManagedUrl && existingOrigin === previousManagedUrl;

    if (legacyOrMissing || managedByPreviousBuild) {
      localStorage.setItem('podwaffle_server_connection', JSON.stringify({
        ...(existing || {}),
        enabled: true,
        baseUrl: url.href.replace(/\\\/+$/, ''),
        host: url.hostname,
        port: url.port || '',
        secure: url.protocol === 'https:',
        source: 'mobile-build',
        updatedAt: new Date().toISOString(),
      }));
      localStorage.setItem(managedBackendKey, url.origin);
    }
  } catch (err) {
    console.warn('[mobile] Invalid backendUrl in server.config.json:', err.message);
  }
})();
`;
  fs.writeFileSync(path.join(targetDir, 'mobile-config.js'), bootstrap, 'utf8');

  const indexPath = path.join(targetDir, 'index.html');
  let indexHtml = fs.readFileSync(indexPath, 'utf8');
  indexHtml = indexHtml
    .replace(/\s*<link rel="preconnect" href="https:\/\/fonts\.googleapis\.com">/g, '')
    .replace(/\s*<link rel="preconnect" href="https:\/\/fonts\.gstatic\.com" crossorigin>/g, '')
    .replace(/\s*<link href="https:\/\/fonts\.googleapis\.com\/css2\?[^\"]+" rel="stylesheet">/g, '')
    .replace(bulmaUrl, 'css/bulma.min.css')
    .replace('<script src="js/api.js"></script>', '<script src="mobile-config.js"></script>\n  <script src="js/api.js"></script>');
  fs.writeFileSync(indexPath, indexHtml, 'utf8');

  const serviceWorkerPath = path.join(targetDir, 'sw.js');
  if (fs.existsSync(serviceWorkerPath)) {
    let serviceWorker = fs.readFileSync(serviceWorkerPath, 'utf8');
    serviceWorker = serviceWorker.replace("  './manifest.json',", "  './manifest.json',\n  './mobile-config.js',\n  './css/bulma.min.css',");
    fs.writeFileSync(serviceWorkerPath, serviceWorker, 'utf8');
  }

  console.log(`[mobile] Copied client assets to ${targetDir}`);
  console.log(`[mobile] Add-on bootstrap: ${backendUrl || 'configured on first launch'}`);
  console.log(`[mobile] Profile bootstrap: ${profileId || 'selected on first launch'}`);
}

main().catch((err) => {
  console.error('[mobile] Failed to prepare web assets:', err);
  process.exitCode = 1;
});
